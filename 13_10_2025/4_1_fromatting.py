# Задача 1
# 1 рубль = 0.0092 евро
# Переведите рубли в евро и выведите с точностью до 2 знаков
# Ожидаемый вывод: "5500 руб = 50.60 €"
rubles = 5500
exchange_rate = 0.0092
print(f"{rubles} руб = {(rubles * exchange_rate):.2f} €\n")


# Задача 2
# Переведите в Фаренгейты по формуле: F = C × 9/5 + 32
# Выведите с точностью до 1 знака
# Ожидаемый вывод: "23.5°C = 74.2°F"
celsius = 23.456
print(f"{celsius:.1f}°C = {(celsius * (9 / 5) + 32):.1f}°F\n")


