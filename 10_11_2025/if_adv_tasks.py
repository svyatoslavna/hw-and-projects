"""

Задача 4. Тернарный оператор с регулярными выражениями

Условие:
Напишите программу, которая проверяет email на валидность используя тернарный оператор и regex.

Логика:

    Если email содержит '@' и '.' -> "Валидный email"
    Иначе -> "Невалидный email"

Пример входных данных:
    Email: "user@example.com"
"""

import re

email = "user@example.com"
valid = re.search(r"@", email) and re.search(r"\.", email)

print("Валидный email" if valid else "Невалидный email")
print("\n")


"""

Задача 5. Сложная проверка с регулярными выражениями

Условие:
Напишите программу, которая анализирует сложность пароля используя тернарный оператор и regex.

Логика:

    Если пароль содержит цифры И 
    буквы в верхнем и нижнем регистре И длиной >= 8 -> "Надежный пароль"
    Иначе если пароль содержит цифры ИЛИ буквы в разных регистрах -> 
    "Средний пароль"  
    Иначе -> "Слабый пароль"

Пример входных данных:
    Пароль: "Secure123"
"""

password = "Secure123"

has_digit = bool(re.search(r"\d", password))
has_upper = bool(re.search(r"[A-Z]", password))
has_lower = bool(re.search(r"[a-z]", password))
has_min_length = len(password) >= 8

# только не очень читаемо:
print(
    "Надежный пароль"
    if has_digit and has_upper and has_lower and has_min_length
    else (
        "Средний пароль"
        if has_digit or (has_upper and has_lower)
        else "Слабый пароль"
    )
)
print("\n")


